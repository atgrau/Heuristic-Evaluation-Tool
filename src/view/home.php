    <h2>Heuristic Evaluation</h2>

    <h3>What is Heuristic Evaluation (HE)?</h3>
    Heuristic Evaluation (HE) is one of the most widely used methods for evaluating
    the usability of an interactive system. This technique became the most popular
    user-centred design approach in the 1990s but has become less prominent with
    the move away from desktop applications. It is an effective method to assess
    user interfaces (UI) by taking the recommendations based on User Centred Design
    (UCD) principles. These recommendations come in terms of design principles, heuristics,
     guidelines, best practices or user interface design patterns and standards
     that are supposed to serve interface designers and evaluators.

